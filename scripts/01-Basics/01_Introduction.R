# R Introduction
# Let's start with some basic R commands.

# Basic Arithmetic Operations
2 + 2  # Addition
7 - 3  # Subtraction
5 * 6  # Multiplication
9 / 3  # Division

# R Getting Started
# A simple R command to display a message
"Hello, R World!"

# R Comments
# Comments are lines that R will ignore during execution.

# The following command outputs the value 10
10

# R Variables and Constants
# Assigning values to variables
x <- 10  # x stores the value 10
y <- 20  # y stores the value 20

# Displaying the values of variables
x  # Output: 10
y  # Output: 20

# Using a constant
PI <- 3.14159
PI  # Output: 3.14159

# R Data Types
# Numeric Data Type
num <- 42.5
num  # Output: 42.5

# Character Data Type
str <- "R Programming"
str  # Output: "R Programming"

# Logical Data Type (TRUE or FALSE)
bool <- TRUE
bool  # Output: TRUE

# Checking the data type of a variable
class(num)  # Output: "numeric"
class(str)  # Output: "character"
class(bool)  # Output: "logical"


# R Print Output
# The value of a variable is displayed directly in the console.

# Displaying variables
name <- "Alice"
age <- 25
name  # Output: "Alice"
age   # Output: 25

# Combining values with paste (R will still show the result)
paste("Name:", name, "Age:", age)  # Output: "Name: Alice Age: 25"

# R Numbers
# Integer
int_val <- 10
int_val  # Output: 10

# Floating-point number (decimal)
float_val <- 10.5
float_val  # Output: 10.5

# Basic arithmetic operations
sum_val <- int_val + float_val
sum_val  # Output: 20.5

# You can also use functions like abs(), sqrt(), and round()
neg_num <- -5
abs(neg_num)  # Output: 5
sqrt(16)      # Output: 4
round(3.14159, 2)  # Output: 3.14




